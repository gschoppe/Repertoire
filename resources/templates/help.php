        <h4 class="helpheader">Preparing Songs</h4>
        <p>
            Repertoire supports several different file formats.
            The process of preparing files for upload will differ,
            depending on the file type.
        </p>
        <h5>Scanned Pages</h5>
        <p>
            To conserve space and speed up the upload process, we
            recommend scanning images at around <?=round(min(MAX_FILE_W/8.5, MAX_FILE_H/11))?>dpi 
            for use in Repertoire.  Images should be saved in the
            PNG, JPEG, or GIF format.
        </p><p>
            Scanning and organizing a large existing collection 
            is the most time-consuming part of switching from
            music binders to Repertoire.  To speed up the process,
            you can have your music scanned for you at many print
            centers like Staples or Kinkos.  However, some will
            refuse to scan sheets that contain a copyright notice.
        </p>
        <h5>ChordPro Files</h5>
        <p>
            Repertoire has basic support for the 
            <a href="http://tenbyten.com/software/songsgen/help/HtmlHelp/files_reference.htm">ChordPro</a> format.
            ChordPro files should be saved as plain text, with the
            extension '.chopro'.
        </p>
        <h5>PDFs</h5>
        <p>
            Repertoire is currently beta testing PDF support.  As such,
            you may experience occasional rendering issues, especially
            on zoom or resize.  If this occurs, simply zoom in one level,
            wait for the re-render to complete, and zoom back out one
            level.
        </p>
        <h5>Text Files</h5>
        <p>
            As a last resort, Repertoire supports plain text files,
            such as those made in notepad.  Files should have the
            extension '.txt'.
        </p>
    </div>
    <div class="helpsection">
        <h4 class="helpheader">Uploading Songs</h4>
        <p>
            Songs can be uploaded individually through the upload
            page.  To upload multiple files, we offer a bulk
            upload option at the bottom of that page.
        </p><p>
            On the bulk upload page, you can select multiple files
            at once in the "browse" dialog box.  Each file will be
            uploaded, using its filename as the song title.  After
            the upload is complete, other song details can be added
            by editing the song.
        </p>
    </div>
    <div class="helpsection">
        <h4 class="helpheader">Finding a Song</h4>
        <p>
            Once you have uploaded your song collection to Repertoire,
            you can find any song directly from the home page.  By 
            default, all of the songs are listed alphabetically, broken
            into pages of ten songs each.  The controls to move from 
            page to page are at the bottom of the table.  However, you 
            can find your song much faster, using a few tricks.
        </p>
        <h5>Sorting</h5>
        <p>
            By clicking any of the table headers on the home page, you
            can sort your songs by that header.  Clicking again will 
            reverse the sort order.  For example, clicking the "Views"
            header once will show you the songs that you view most often.
        </p>
        <h5>Searching</h5>
        <p>
            On the top left of the homepage, there is a search bar.  If
            you click into the text box and type a few characters of the
            song's title, it will immediately be selected in the table.
            Search resultscan be cleared by deleting the contents of the 
            search box.
        </p>
        <h5>Increasing Page Size</h5>
        <p>
            By default, Repertoire shows 10 songs per page.  By selecting
            a different number at the top left of the home page, you can
            increase the number of songs shown at once to up to 100 songs
            per page.  If you prefer to see all of your songs on one page,
            the pagination setting can be disabled by altering your
            advanced settings.
        </p>
    </div>
    <div class="helpsection">
        <h4 class="helpheader">Viewing a Song</h4>
        <p>
            Once you have selected a song, additional controls become
            available in the header, and by keyboard or gesture.
        </p>
        <h5>Scrolling</h5>
        <p>
            When viewing a song, you can control scrolling by mouse, 
            scrollbar, swipe, or via an external foot pedal.
        </p>
        <h5>Zoom Controls</h5>
        <p>
            When viewing a song, three controls appear in the top
            left corner of the page.  The minus (-) button will zoom out,
            The plus (+) button will zoom in, and clicking the zoom level
            in the center will reset zoom to 100%. 
        </p>
        <h5>Set List Controls</h5>
        <p>
            When viewing a song within a set list, two controls will
            appear in the top center of the page.  The left arrow button
            (&laquo;) will go to the previous song in the set.  The right
            arrow button (&raquo;) will go to the next song in the set.
        </p><p>
            On a touchscreen device, swiping right to left will go back
            one song.  Swiping left to right will go forwards one song.
        </p><p>
            If using an external foot pedal to control scrolling, pressing
            the left button when scrolled all the way to the top of the page
            will go back one song.  Pressing the right button when scrolled
            all the way to the bottom of the page will go forwards one song.
        </p>
    </div>
    <div class="helpsection">
        <h4 class="helpheader">Set Lists</h4>
        <p>
            Repertoire allows users to create set lists of songs, to make
            stage performances easier.  Lists can be created, managed, and
            deleted from the 'Set Lists' tab.
        </p>
        <h5>Adding Songs to a Set List</h5>
        <p>
            To add a song to a list, first find the song on the main page.
            Then, click the "add to set" button
            (<img src="resources/graphics/add.png" class="icon"/>)
            on the right.  Finally, click the "add to set" button next to 
            the list you want to add the song to.
        </p>
          <h5>Adding Songs to a Set List</h5>
        <p>
            To add a song to a list, first find the song on the main page.
            Then, click the "add to set" button
            (<img src="resources/graphics/add.png" class="icon"/>)
            on the right.  Finally, click the "add to set" button next to 
            the list you want to add the song to.
        </p>
        
