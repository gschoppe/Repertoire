    <div class="helpsection">
        <h4 class="helpheader">Accessing Remotely</h4>
        <p>
            Once Repertoire is running on your device, it can 
            be accessed remotely by other machines on your 
            local network.  This makes sharing your library 
            with a group easier, and also simplifies managing
            songs.
        </p>
        </p>
            To access Repertoire remotely, open the browser
            on the remote device and type the ip address and
            port of the device running Repertoire into the
            address bar.  Depending on your server, the ip
            and port used to access Repertoire may vary.
        </p>
        <p>
            Repertoire cannot necessarily identify the 
            proper IP address and port to enter, but the
            address is most likely:
            <code><?=get_server_address()?></code>.
        </p>
    </div>
     <div class="helpsection">
        <h4 class="helpheader">External Foot Pedal</h4>
        <p>
            Repertoire supports scrolling and navigating
            set lists with a custom usb foot pedal that 
            emulates the left and right arrow keys.
        </p><p>
            You can find the old instructions for 
            building such a pedal on <a href="http://www.instructables.com/id/USB-Foot-Pedal/">instructables.</a>
            However, the process has recently 
            become much easier. Simply solder two 
            buttons to the arrow keys on a 
            <a href="http://www.makeymakey.com/">MakeyMakey</a> ($50), 
            and optionally build a case to protect it.
        </p><p>
            If you have any experience with micro-controllers,
            it is also fairly trivial to build and program a pedal
            with an 
            <a href="http://arduino.cc/en/Main/arduinoBoardLeonardo">
            Arduino Leonardo</a> ($25) or any Leonardo-compatible
            microcontroller ($7-10).
        </p><p>
            Depending on demand, I may design a converter box,
            to allow players to use any musicians dual footswitch
            with Repertoire, or I may design a short-run commercial
            footswitch.
        </p>
